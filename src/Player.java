
public class Player {
	
	private String username;
	private String color;
	
	// Setters
	public void setUsername(String username) {
		this.username = username;
	}
	public void setColor(String color) {
		this.color = color;
	}
	// Getters
	public String getUsername() {
		return username;
	}
	public String getColor() {
		return color;
	}

}